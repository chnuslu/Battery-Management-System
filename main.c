void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
    if (hadc == &hadc1)
    {
        // Gerilim değeri
        uint16_t voltage_adc_value = ADC_BUFFER[0]; // DMA tamamlandığında gerilim değeri alınır
        voltage = (float) (voltage_adc_value)*ADC_REFERENCE_VOLTAGE *2 / ADC_RESOLUTION*1.3;


        // Akım değeri
        uint16_t current_adc_value = ADC_BUFFER[1]; // DMA tamamlandığında akım değeri alınır
        float rawVoltage = (float)current_adc_value * ADC_REFERENCE_VOLTAGE *2 / ADC_RESOLUTION*0.95 ;
        current = (rawVoltage - 2.5)/0.185; // Sens: 5A -> 0.185,

        //ADC değerinin doğruluğunu artırmak için çarptığımız katsayıyı hesaplamak için aşağıdaki belgeden yararladık.
        // https://www.st.com/resource/en/application_note/cd00211314-how-to-get-the-best-adc-accuracy-in-stm32-microcontrollers-stmicroelectronics.pdf


        // Sıcaklık değeri
        uint16_t temperature_adc_value = ADC_BUFFER[2];
        temp =   temperature_adc_value*0.030;


    }
}



